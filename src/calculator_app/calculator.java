    private void bAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAddActionPerformed
        int opSize = lOperation.getText().length();
        
        if (opSize == 0) {
            if (tfResult.getText().length() != 0) {
                if ( !(tfResult.getText().length() == 1 && tfResult.getText().contains("-")) ) {
                    num = Double.parseDouble(tfResult.getText());
                    lOperation.setText(tfResult.getText() + "+");
                    tfResult.setText("");
                    select = 1;
                }
            }
        }
        else {
            if (tfResult.getText().length() != 0) {
                operation();
                num = Double.parseDouble(tfResult.getText());
                lOperation.setText(tfResult.getText() + "+");
                tfResult.setText("");
                select = 1;
            }
            else {

                String text;

                StringBuilder changeS = new StringBuilder(lOperation.getText());
                changeS.setCharAt(opSize-1, '+');
                text = changeS.toString();
                lOperation.setText(text);
                select = 1;
            }
        }
    }//GEN-LAST:event_bAddActionPerformed

    private void bSubActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSubActionPerformed
        int opSize = lOperation.getText().length();
        
        if (opSize == 0) {
            if ( (tfResult.getText().length() != 0) && !(tfResult.getText().length() == 1 && tfResult.getText().contains("-")) ) {
                num = Double.parseDouble(tfResult.getText());
                lOperation.setText(tfResult.getText() + "-");
                tfResult.setText("");
                select = 2;
            }
            else {
                tfResult.setText("-");
            }
        }
        else {
            if (tfResult.getText().length() != 0) {
                operation();
                num = Double.parseDouble(tfResult.getText());
                lOperation.setText(tfResult.getText() + "-");
                tfResult.setText("");
                select = 2;
            }
            else {
        
                String text;

                StringBuilder changeS = new StringBuilder(lOperation.getText());
                changeS.setCharAt(opSize-1, '-');
                text = changeS.toString();
                lOperation.setText(text);
                select = 2;
            }
        }
    }//GEN-LAST:event_bSubActionPerformed

