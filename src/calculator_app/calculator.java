    public void operation() {
        if (num == 0) {
            
        }
        switch(select){
            case 1: //Add
                result = num + Double.parseDouble(tfResult.getText());
                tfResult.setText(String.format( "%.2f", result ));
                
                break;
            case 2: //Subtract
                result = num - Double.parseDouble(tfResult.getText());
                tfResult.setText(String.format( "%.2f", result ));
                
                break;
                
            case 3: //Multiply
                result = num * Double.parseDouble(tfResult.getText());
                tfResult.setText(String.format( "%.2f", result ));
                
                break;
                
            case 4: //Divide
                result = num / Double.parseDouble(tfResult.getText());
                tfResult.setText(String.format( "%.2f", result ));
                
                break;
        }
        
    }

